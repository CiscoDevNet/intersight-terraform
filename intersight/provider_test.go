func testAccPreCheck(t *testing.T) {
	variables := []string{
		"INTERSIGHT_API_KEY",
		"INTERSIGHT_SECRET_KEY_FILE_PATH",
		"INTERSIGHT_ENDPOINT_URL",
	}

	for _, variable := range variables {
		value := os.Getenv(variable)
		if value == "" {
			t.Fatalf("`%s` must be set for acceptance tests!", variable)
		}
	}
}
